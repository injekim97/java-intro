/* package를 만들었다면 해당 패키지에 들어가는 자바 파일 첫줄에 아래와 같이 선언해줘야 함 */
package variable;

public class Var5 {

    public static void main(String[] args) {

        int a;                  // 변수 선언
        a = 1;                  // 변수 초기화
        System.out.println(a);

        int b = 2;              // 변수 선언 및 초기화
        System.out.println(b);

        int c = 7, d = 55;       // 여러 변수 선언 및 초기화
        System.out.println(c);
        System.out.println(d);
    }
}