/* package를 만들었다면 해당 패키지에 들어가는 자바 파일 첫줄에 아래와 같이 선언해줘야 함 */
package variable;

public class Var3 {

    public static void main(String[] args) {
        int a;                                // 변수(변할 수 있는 값) 선언
        a = 777;                              // 변수 초기화

        System.out.println(a);

        a = 50;
        System.out.println(a);
        System.out.println(a);
    }
}